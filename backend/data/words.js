/**
 * Get flat word list filtered by difficulty
 * @param {"easy"|"medium"|"hard"|"all"} difficulty
 * @returns {string[]}
 */
function getWordsByDifficulty(difficulty = "all") {
  const result = [];
  for (const cat of Object.values(WORDS)) {
    if (difficulty === "all") {
      result.push(...cat.easy, ...cat.medium, ...cat.hard);
    } else if (cat[difficulty]) {
      result.push(...cat[difficulty]);
    }
  }
  return [...new Set(result)]; // dedupe
}

